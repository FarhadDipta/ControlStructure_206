nilai = int(input("Masukan Nilai Siswa : "))

if nilai >= 90 :
    print("Kinerja siswa sangat baik")
elif nilai >= 80:
    print("Kinerja siswa sangat baik")
elif nilai >= 70 :
    print("Kinerja siswa baik")
elif nilai >= 60 :
    print("Kinerja siswa rata-rata")
else :
    print("Kinerja siswa kurang")